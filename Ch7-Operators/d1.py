x = 5
y = 3

# 5+3
print(x + y)  # Output:8

# 5-3
print(x - y)  # Output:2

# 5*3
print(x * y)  # Output:15

# 5/3
print(x / y)  # Output:1.6666666666666667

# 5%3=Reminder of 5/3
print(x - y)  # Output:2

# 5*5*5
# same as x^y
print(x**y)  # Output:125


